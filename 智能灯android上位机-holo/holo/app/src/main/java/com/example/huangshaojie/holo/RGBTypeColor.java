public class RGBTypeColor {
    private float R;
    private float G;
    private float B;

    public RGBTypeColor(float r,float g,float b){
        this.R=r;
        this.G=g;
        this.B=b;
    }

    public float getR(){
        return this.R;
    }

    public float getG() { return this.G; }

    public float getB(){
        return this.B;
    }
}
